package model.manager.reports;

public class PacienteReferidoDeVoltaXLS {

    private String nid;

    private String nome;

    private String idade;

    private String dataUltimaReferencia;

    private String dataUltimaDispensa;

    private String farmaciaReferencia;

    private String dataRetorno;

    private String notas;

    public String getNid() {
        return nid;
    }

    public void setNid(String nid) {
        this.nid = nid;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getIdade() {
        return idade;
    }

    public void setIdade(String idade) {
        this.idade = idade;
    }

    public String getDataUltimaReferencia() {
        return dataUltimaReferencia;
    }

    public void setDataUltimaReferencia(String dataUltimaReferencia) {
        this.dataUltimaReferencia = dataUltimaReferencia;
    }

    public String getDataUltimaDispensa() {
        return dataUltimaDispensa;
    }

    public void setDataUltimaDispensa(String dataUltimaDispensa) {
        this.dataUltimaDispensa = dataUltimaDispensa;
    }

    public String getFarmaciaReferencia() {
        return farmaciaReferencia;
    }

    public void setFarmaciaReferencia(String farmaciaReferencia) {
        this.farmaciaReferencia = farmaciaReferencia;
    }

    public String getDataRetorno() {
        return dataRetorno;
    }

    public void setDataRetorno(String dataRetorno) {
        this.dataRetorno = dataRetorno;
    }

    public String getNotas() {
        return notas;
    }

    public void setNotas(String notas) {
        this.notas = notas;
    }
}
