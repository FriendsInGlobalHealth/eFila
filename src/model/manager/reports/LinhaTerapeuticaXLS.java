package model.manager.reports;

public class LinhaTerapeuticaXLS {

    private String codigoRegime;
    private String regime;
    private String activo;
    private String contagem;

    public String getCodigoRegime() {
        return codigoRegime;
    }

    public void setCodigoRegime(String codigoRegime) {
        this.codigoRegime = codigoRegime;
    }

    public String getRegime() {
        return regime;
    }

    public void setRegime(String regime) {
        this.regime = regime;
    }

    public String getActivo() {
        return activo;
    }

    public void setActivo(String activo) {
        this.activo = activo;
    }

    public String getContagem() {
        return contagem;
    }

    public void setContagem(String contagem) {
        this.contagem = contagem;
    }
}
