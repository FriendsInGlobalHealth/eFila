package model.manager.reports;

public class MmiaStock {

    private String fnm;

    private String medicamento;

    private String quantidadeFrasco;

    private String saldo;

    private String entrdas;

    private String saidas;

    private String perdasAjustes;

    private String inventario;

    private String validade;

    public String getFnm() {
        return fnm;
    }

    public void setFnm(String fnm) {
        this.fnm = fnm;
    }

    public String getMedicamento() {
        return medicamento;
    }

    public void setMedicamento(String medicamento) {
        this.medicamento = medicamento;
    }

    public String getQuantidadeFrasco() {
        return quantidadeFrasco;
    }

    public void setQuantidadeFrasco(String quantidadeFrasco) {
        this.quantidadeFrasco = quantidadeFrasco;
    }

    public String getSaldo() {
        return saldo;
    }

    public void setSaldo(String saldo) {
        this.saldo = saldo;
    }

    public String getEntrdas() {
        return entrdas;
    }

    public void setEntrdas(String entrdas) {
        this.entrdas = entrdas;
    }

    public String getSaidas() {
        return saidas;
    }

    public void setSaidas(String saidas) {
        this.saidas = saidas;
    }

    public String getPerdasAjustes() {
        return perdasAjustes;
    }

    public void setPerdasAjustes(String perdasAjustes) {
        this.perdasAjustes = perdasAjustes;
    }

    public String getInventario() {
        return inventario;
    }

    public void setInventario(String inventario) {
        this.inventario = inventario;
    }

    public String getValidade() {
        return validade;
    }

    public void setValidade(String validade) {
        this.validade = validade;
    }
}
