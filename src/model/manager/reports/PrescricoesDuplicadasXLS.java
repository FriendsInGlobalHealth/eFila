package model.manager.reports;

public class PrescricoesDuplicadasXLS {

    private String nid;

    private String nome;

    private String dataPrescricao;

    private String tipoPaciente;

    private String regimeTerapeutico;

    private String linha;

    private String tipoDispensa;

    private String notas;

    public String getNid() {
        return nid;
    }

    public void setNid(String nid) {
        this.nid = nid;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getDataPrescricao() {
        return dataPrescricao;
    }

    public void setDataPrescricao(String dataPrescricao) {
        this.dataPrescricao = dataPrescricao;
    }

    public String getTipoPaciente() {
        return tipoPaciente;
    }

    public void setTipoPaciente(String tipoPaciente) {
        this.tipoPaciente = tipoPaciente;
    }

    public String getRegimeTerapeutico() {
        return regimeTerapeutico;
    }

    public void setRegimeTerapeutico(String regimeTerapeutico) {
        this.regimeTerapeutico = regimeTerapeutico;
    }

    public String getLinha() {
        return linha;
    }

    public void setLinha(String linha) {
        this.linha = linha;
    }

    public String getTipoDispensa() {
        return tipoDispensa;
    }

    public void setTipoDispensa(String tipoDispensa) {
        this.tipoDispensa = tipoDispensa;
    }

    public String getNotas() {
        return notas;
    }

    public void setNotas(String notas) {
        this.notas = notas;
    }
}
